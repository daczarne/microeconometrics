#### COMPARISON OPERATORS ####

# Greater than
print(1 > 2)
# Less than
print(1 < 2)
# Greater than or Equal to
print(1 >= 1)
# Less than or Equal to
print(1 <= 4)
# Equality
print(1 == 1)
print(1 == "1")
print('hi' == 'bye')
# Inequality
print(1 != 2)

#### LOGICAL OPERATORS ####

# AND
print((1 > 2) and (2 < 3))

# OR
print((1 > 2) or (2 < 3))

# Multiple logical operators
print((1 == 2) or (2 == 3) or (4 == 4))
